package atif.dsa.linkedList.all.practice.removeDuplicate;

import java.util.HashSet;
import java.util.Set;

public class RemoveDuplicateLinkedList {
    private Node head;
    private int length;

    class Node {
        int value;
        Node next;

        Node(int value) {
            this.value = value;
        }
    }

    public RemoveDuplicateLinkedList(int value) {
        Node newNode = new Node(value);
        head = newNode;
        length = 1;
    }

    public Node getHead() {
        return head;
    }

    public int getLength() {
        return length;
    }

    public void printList() {
        Node temp = head;
        while (temp != null) {
            System.out.println(temp.value);
            temp = temp.next;
        }
    }

    public void printAll() {
        if (length == 0) {
            System.out.println("Head: null");
        } else {
            System.out.println("Head: " + head.value);
        }
        System.out.println("Length:" + length);
        System.out.println("\nLinked List:");
        if (length == 0) {
            System.out.println("empty");
        } else {
            printList();
        }
    }

    public void makeEmpty() {
        head = null;
        length = 0;
    }

    public void append(int value) {
        Node newNode = new Node(value);
        if (head == null) {
            head = newNode;
        } else {
            Node current = head;
            while (current.next != null) {
                current = current.next;
            }
            current.next = newNode;
        }
        length++;
    }

    public void removeDuplicate() {
        Set<Integer> uniqueNodeValues = new HashSet<>();
        Node current = head;
        Node pre = null;
        while (current != null) {
            if (uniqueNodeValues.contains(current.value)) {
                pre.next = current.next;
                length--;
            } else {
                uniqueNodeValues.add(current.value);
                pre = current;
            }
            current = current.next;
        }
    }
}
